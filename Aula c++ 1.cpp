#include <iostream>

using namespace std;

int main() {
	float nota1, nota2, media;
	
	cout << "Informe a primeira nota: ";
	cin >> nota1;
	
	//command out: 
	cout << "Informe a segunda nota: ";
	//command in:
	cin >> nota2;
	
	//Declarando a variável 'média'	
	media = (nota1 * 2 + nota2 *3) / 5;
	
	//Dando a resposta:
	cout << "Media ponderada das notas: " << media << endl;
	
	//Conferindo se a nota é um número
	if (cin.fail()) {
		cout << "A nota deve ser escrita em numero";
		return 1;
	}	
	
	return 0;
}
